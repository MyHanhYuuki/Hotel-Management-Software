    private void btn_XacNhanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_XacNhanActionPerformed
        //lấy dữ liệu từ các trường
        String ma = txt_MaDV.getText();
        String ten = txt_TenDV.getText();
        String gia = txt_GiaDV.getText();
        String ghichu = cbb_GhiChu.getSelectedItem().toString();
        //ghi dữ liệu
        if(dichvuBLL.themDichVu(ma, ten, gia, ghichu)){
        //thông báo
        JOptionPane.showMessageDialog(this, "Thêm thành công");
        //chuẩn hóa lại nút và trường cho thích hợp
        txt_MaDV.setText("");
        txt_TenDV.setText("");
        txt_GiaDV.setText("");
        cbb_GhiChu.removeAllItems();
        btn_Them.setEnabled(true);
        //load lại dữ liệu
        control.bindingDichVu(table_DSDichVu, dichvuBLL.getAll());
        }
    }//GEN-LAST:event_btn_XacNhanActionPerformed

