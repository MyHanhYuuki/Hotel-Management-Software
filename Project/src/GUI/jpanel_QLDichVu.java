    private void table_DSDichVuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_DSDichVuMouseClicked
        int index = table_DSDichVu.getSelectedRow();
        //đổ dữ liệu sang thông tin
        txt_MaDV.setText(table_DSDichVu.getValueAt(index, 0).toString());
        txt_TenDV.setText(table_DSDichVu.getValueAt(index, 1).toString());
        txt_GiaDV.setText(table_DSDichVu.getValueAt(index, 2).toString());
        //txt_GhiChu.setText(table_DSDichVu.getValueAt(index, 3).toString());
        cbb_GhiChu.removeAllItems();
        cbb_GhiChu.addItem("Sẵn sàng");
        cbb_GhiChu.addItem("Hết hàng");
        cbb_GhiChu.setSelectedItem(table_DSDichVu.getValueAt(index, 3).toString());
        
        //hiện nút sửa xóa
        btn_Sua.setEnabled(true);
        //btn_Xoa.setEnabled(true);
    }//GEN-LAST:event_table_DSDichVuMouseClicked

    private void btn_SuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SuaActionPerformed
        //lấy dữ liệu từ panel_ThongTin
        String maDV = txt_MaDV.getText();
        int row = table_DSDichVu.getSelectedRow();
        boolean kiemtra = true;
        //kiểm tra nếu mã mới sửa trùng với mã cũ thì thông báo
        int rows = table_DSDichVu.getRowCount();
        for(int i = 0; i < rows; i++){
            if(i == row)
                continue;
            if(maDV.equals(table_DSDichVu.getValueAt(i, 0)))
            {
                JOptionPane.showMessageDialog(this, "Mã dịch vụ bị trùng");
                kiemtra = false;
            }
        }
        if(kiemtra == true){
            String tenDV = txt_TenDV.getText();
            String giaDV = txt_GiaDV.getText();
            String ghiChu = cbb_GhiChu.getSelectedItem().toString();
            
            String macu = table_DSDichVu.getValueAt(row, 0).toString();
            //sửa  dữ liệu 
            if(dichvuBLL.suaDichVu(maDV, tenDV, giaDV, ghiChu, macu)){
            //thông báo
            JOptionPane.showMessageDialog(this, "Sửa thành công");
            //load lại dữ liệu lên bảng
            control.bindingDichVu(table_DSDichVu, dichvuBLL.getAll());
            //ẩn các nút
            btn_Sua.setEnabled(false);
            //btn_Xoa.setEnabled(false);
            //chuẩn hóa các trường nhập
            txt_MaDV.setText("");
            txt_TenDV.setText("");
            txt_GiaDV.setText("");
            cbb_GhiChu.removeAllItems();
            }
        }
    }//GEN-LAST:event_btn_SuaActionPerformed

